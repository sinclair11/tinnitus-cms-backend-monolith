package com.tinnitussounds.cms.dashboard;

public class DashboardResources {
    private Integer albums;
    private Integer songs;
    private Integer samples;
    private Integer presets;
    private Integer categories;

    public Integer getAlbums() {
        return albums;
    }
    public void setAlbums(Integer albums) {
        this.albums = albums;
    }
    public Integer getSongs() {
        return songs;
    }
    public void setSongs(Integer songs) {
        this.songs = songs;
    }
    public Integer getSamples() {
        return samples;
    }
    public void setSamples(Integer samples) {
        this.samples = samples;
    }
    public Integer getPresets() {
        return presets;
    }
    public void setPresets(Integer presets) {
        this.presets = presets;
    }
    public Integer getCategories() {
        return categories;
    }
    public void setCategories(Integer categories) {
        this.categories = categories;
    }
}

